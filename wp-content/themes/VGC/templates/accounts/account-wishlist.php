                    function addWItemToCart(itemId) {
                        var url = '';
                        url = url.gsub('%item%', itemId);
                        var form = $('wishlist-view-form');
                        if (form) {
                            var input = form['qty[' + itemId + ']'];
                            if (input) {
                                var separator = (url.indexOf('?') >= 0) ? '&' : '?';
                                url += separator + input.name + '=' + encodeURIComponent(input.value);
                            }
                        }
                        setLocation(url);
                    }

